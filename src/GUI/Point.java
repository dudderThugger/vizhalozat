package GUI;
/**
 * A Játékpanelen tárolanód elemek koordinátákat tároló osztály.
 */
public class Point {
    /**
     * Az elem x koordinátája
     */
    public int x;
    /**
     * Az elem y koordinátája
     */
    public int y;

    /**
     * A Point osztály konstruktora
     * @param x x koordináta
     * @param y y koordináta
     */
    public Point(int x, int y) { this.x = x; this.y = y; }
}
